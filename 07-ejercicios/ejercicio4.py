"""
Ejercicio 4.
    Pedir dos numeros al usuario y hacer todas las operaciones 
    basicas de una calculadora y mostralo en pantalla
"""


print(" **** C A L C U L A D O R A ***")
numero_1 = int(input("Introduce el primer numero: "))

numero_2 = int(input("Introduce el segundo numero: "))

print(f"La suma de {str(numero_1)} + {str(numero_2)} es {str(numero_1 + numero_2)}")

print(f"La resta de {str(numero_1)} - {str(numero_2)} es {str(numero_1 - numero_2)}")

print(f"La multiplicacion de {str(numero_1)} * {str(numero_2)} es {str(numero_1 * numero_2)}")

print(f"La division de {str(numero_1)} / {str(numero_2)} es {str(numero_1 / numero_2)}")

print(f"El modulo de {str(numero_1)} % {str(numero_2)} es {str(numero_1 % numero_2)}")

